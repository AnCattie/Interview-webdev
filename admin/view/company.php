Oorspronkelijk werd de groep opgericht voor en door technologie mensen met de uitdrukkelijke bedoeling om die mensen te helpen doorgroeien indien gewenst tot en met het ondernemerschap toe. <br><br>

Ondertussen is dit mission statement verbreed en is de groep verrijkt met mensen uit de creatieve sector die goed communiceren met de ‘business mensen’ <br>
en die perfect samenwerken met Cronos IT-mensen om tot de meest creatieve en technologisch beste oplossing voor klanten te komen. <br><br>

De groep is dan ook continu op zoek naar creatieve, gedreven mensen en is altijd bereid te luisteren naar innovatieve ideeën van potentiële ondernemers. <br>

